n = int(input("Ingrese un numero: "))

if n == 1:
    print("Hoy es lunes")
elif n == 2:
    print("Hoy es martes")
elif n == 3:
    print("Hoy es miercoles")
elif n == 4:
    print("Hoy es jueves")
elif n == 5:
    print("Hoy es viernes")
elif n == 6:
    print("Hoy es sabado")
elif n == 7:
    print("Hoy es domingo")
else:
    print("Numero no valido")